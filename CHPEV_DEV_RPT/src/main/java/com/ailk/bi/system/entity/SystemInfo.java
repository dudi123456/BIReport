package com.ailk.bi.system.entity;

public class SystemInfo {
	String system_id = "";
	String system_name = "";
	String system_desc = "";
	String system_url = "";
	String status = "";
	String sequence = "";

	public String getSystem_id() {
		return system_id;
	}

	public void setSystem_id(String system_id) {
		this.system_id = system_id;
	}

	public String getSystem_name() {
		return system_name;
	}

	public void setSystem_name(String system_name) {
		this.system_name = system_name;
	}

	public String getSystem_desc() {
		return system_desc;
	}

	public void setSystem_desc(String system_desc) {
		this.system_desc = system_desc;
	}

	public String getSystem_url() {
		return system_url;
	}

	public void setSystem_url(String system_url) {
		this.system_url = system_url;
	}

	public String getStatus() {
		return status;
	}

	public void setStatus(String status) {
		this.status = status;
	}

	public String getSequence() {
		return sequence;
	}

	public void setSequence(String sequence) {
		this.sequence = sequence;
	}

}
