package com.ailk.bi.system.entity;

public class InfoMenu {
	private String menu_code;
	private String parent_menu_code;
	private String menu_Row;
	private String menu_Title;
	private String menu_Col;
	private String menu_Url;

	public String getMenu_code() {
		return menu_code;
	}

	public void setMenu_code(String menu_code) {
		this.menu_code = menu_code;
	}

	public String getParent_menu_code() {
		return parent_menu_code;
	}

	public void setParent_menu_code(String parent_menu_code) {
		this.parent_menu_code = parent_menu_code;
	}

	public String getMenu_Row() {
		return menu_Row;
	}

	public void setMenu_Row(String menu_Row) {
		this.menu_Row = menu_Row;
	}

	public String getMenu_Title() {
		return menu_Title;
	}

	public void setMenu_Title(String menu_Title) {
		this.menu_Title = menu_Title;
	}

	public String getMenu_Col() {
		return menu_Col;
	}

	public void setMenu_Col(String menu_Col) {
		this.menu_Col = menu_Col;
	}

	public String getMenu_Url() {
		return menu_Url;
	}

	public void setMenu_Url(String menu_Url) {
		this.menu_Url = menu_Url;
	}

}
