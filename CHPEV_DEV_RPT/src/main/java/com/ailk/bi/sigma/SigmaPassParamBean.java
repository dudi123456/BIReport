package com.ailk.bi.sigma;

public class SigmaPassParamBean {
	private String passParamName;
	private String passParamValue;
	private String passParamDesc;

	public String getPassParamName() {
		return passParamName;
	}

	public void setPassParamName(String passParamName) {
		this.passParamName = passParamName;
	}

	public String getPassParamValue() {
		return passParamValue;
	}

	public void setPassParamValue(String passParamValue) {
		this.passParamValue = passParamValue;
	}

	public String getPassParamDesc() {
		return passParamDesc;
	}

	public void setPassParamDesc(String passParamDesc) {
		this.passParamDesc = passParamDesc;
	}

}
